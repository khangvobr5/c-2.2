#include <stdio.h>

int main() {
    // Declare and initialize two integer variables
    int num1 = 5;  // 'num1' is an integer variable with value 5
    int num2 = 10; // 'num2' is an integer variable with value 10

    // Declare another integer variable to store the sum
    int sum = 0;   // 'sum' is an integer variable, initially set to 0

    // Calculate the sum of num1 and num2, and store the result in 'sum'
    sum = num1 + num2; // sum = 5 + 10

    // Print the result
    printf("The sum of %d and %d is: %d\n", num1, num2, sum);

    return 0;
}
